package com.muflihundev.tspppb;

public class Wisata {
    private int imageRes;
    private String name;

    public Wisata(int imageRes, String name) {
        this.imageRes = imageRes;
        this.name = name;
    }

    public int getImageRes() {
        return imageRes;
    }

    public String getName() {
        return name;
    }
}

